            <h1 data-i18n="history-title">Historia</h1>
            <h3>Red Acelerográfica UAM</h3>
            <p data-i18n="history-description-p1">
                Los sucesos del terremoto del 19 de septiembre de 1985 y las catástrofes derivadas de él, concientizaron a las
                instituciones encargadas de salvaguardar la integridad física de la población, de la necesidad de protocolos tanto de
                prevención como de contención de desastres sísmicos. En el sismo de Michoacán de septiembre 1985 (M=8.1), la
                Universidad Autónoma Metropolitana, Unidad Azcapotzalco (UAM-Azcapotzalco), desarrolló una serie de proyectos de
                investigación sobre vulnerabilidad sísmica, sobre reparación de estructuras y sobre peligro y riesgo sísmico. Desde
                entonces a la fecha se han desarrollado de manera continua proyectos relacionados con la Ingeniería Sísmica.
            </p>
            <p data-i18n="history-description-p2">
                Desde el año de 1993, los académicos del Área de Estructuras del Departamento de Materiales de la División de
                Ciencias Básicas e Ingeniería de la UAM-Azcapotzalco han instalado y operado varias estaciones acelerográficas en
                todas las Unidades Académicas y en la Rectoría General para registrar el movimiento fuerte del terreno debido a los
                sismos que afectan a las instalaciones de la UAM. Se ha recopilado información importante y los equipos han sido
                reemplazados cuando han llegado al término de su vida útil.
            </p>
            <p data-i18n="history-description-p3">
                La Red Acelerográfica de la UAM (RA-UAM) se instaló con la finalidad de ampliar el conocimiento sobre la sismicidad
                local y regional, asignar el riesgo sísmico en cada Unidad Académica de la UAM, así como coadyuvar a tomar medidas
                inmediatas de protección a la población universitaria después de sismos de intensidad importante, de facilitar los
                estudios de peligro y riesgo sísmico, y para mitigar los daños en las edificaciones ante futuros terremotos.
            </p>
            <p data-i18n="history-description-p4">
                En el periodo de 1994-2005 la UAM fomentó y lidereó la creación de la Red Interuniversitaria de Instrumentación
                Sísmica (RIIS), de la cual formaron parte algunas instituciones académicas de la República Mexicana. A partir de 2019
                la RE-UAM forma parte de la Red Sísmica de la Ciudad de México (RSCDMX). El constante monitoreo en los
                principales centros urbanos del país del movimiento del terreno, provocado por sismos intensos, ha provisto de
                información de eventos importantes que son la base para estudios de sismicidad a nivel local y nacional.
            </p>
