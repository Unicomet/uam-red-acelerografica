                        Desde el año de 1993, los académicos del Departamento de Materiales de la División de Ciencias Básicas e Ingeniería de la UAM-Azcapotzalco han instalado y operado estaciones acelerógraficas, para medir el movimiento fuerte de los sismos que afectan a
                        las instalaciones de la UAM. Además, se instaló esta red (RA-UAM) con la finalidad de ampliar el conocimiento sobre la sismicidad local y regional, lo que permitiría mejorar el Reglamento de Construcciones y disminuir los daños
                        en las edificaciones ante futuros terremotos. Desde entonces hasta la fecha se han mantenido en operación algunas estaciones las cuales han estado en operación y han sido reemplazados los equipos cuando han llegado al término de
                        su vida útil; se ha recopilado información. Cabe mencionar que en el año de 1994 la UAM fomentó y lidereó la creación de la Red Interuniversitaria de Instrumentación Sísmica (RIIS), de la cual formaron parte algunas instituciones
                        académicas de la República Mexicana. El constante monitoreo en los principales centros urbanos del país del movimiento del terreno, provocado por sismos intensos, ha provisto de información de eventos importantes que son la base
                        para estudios a nivel local y nacional. En la actualidad continúa la operación de la Red Acelerográfica de la UAM, RA-UAM.
